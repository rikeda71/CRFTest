# 付けたタグを使って、素性を返す
def sent2prediction(sent, distance, tag):
    array = []
    for i in range(len(sent)):
        if i == distance:
            array.append(word2prediction(sent, i, tag))
        else:
            array.append(word2features(sent, i))
    return array


def sent2features(sent):
    return [word2features(sent, i) for i in range(len(sent))]


